package Lesson4;

// 入門問題
// 4-A-3 ~ 4-A-4

public class Lesson4Sample4 {
    public static void main(String[] args) {
        int num1 = 73;
        int num2 = 37;

        System.out.println("num1とnum2にいろいろな演算を行います。");
        System.out.println("num1+num2は" + (num1 + num2) + "です。");
        System.out.println("num1-num2は" + (num1 - num2) + "です。");
        System.out.println("num1*num2は" + (num1 * num2) + "です。");
        System.out.println("num1/num2は" + (num1 / num2) + "です。");
        System.out.println("num1%num2は" + (num1 % num2) + "です。");
        System.out.println("num1とnum2ではnum2の方が大きいですか？" + (num1 < num2) + "です。");
        System.out.println("num1とnum2は同じ数字ではありませんか？" + (num1 != num2) + "です。");
    }
}
