package Lesson4;

// Lesson4 入門問題
// 4-A-7 ~ 4-A-8

public class Lesson4Sample10 {
    public static void main(String[] args) {
        int d = 2;
        double pi = 3.14;
        System.out.println("直径が" + d + "センチの円の");
        System.out.println("円周は" + (d * pi) + "センチです。");

        double a = 5.5;
        int b = 7;
        System.out.println("底辺が" + a + "センチ、高さが" + b + "センチの");
        System.out.println("三角形の面積は" + (a * b / 2) + "平方センチメートルです。");
    }
}
