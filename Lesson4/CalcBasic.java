public class CalcBasic {
    public static void main(String[] args) {
        int num1 = 3;
        int num2 = 19;
        int num3 = 2;
        int num4 = 11;
        int num5 = 5;
        System.out.println((num1 + num2 * num3 - num4) / num5);

        System.out.println((3 + 19 * 2 - 11) / 5);
    }
}
