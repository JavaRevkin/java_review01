package Lesson6;

// Lesson6 挑戦問題
// 6-C-1

public class PowNumber_99R {
    public static void main(String[] args) {
        for (int i = 9; i >= 1; i --) {
            for (int j = 9; j >= 1; j --) {
                System.out.println(i + "*" + j + "=" + i * j);
            }            
        }
    }
}
