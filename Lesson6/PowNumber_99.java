package Lesson6;

// Lesson6 練習問題
// 6-B-5

public class PowNumber_99 {
    public static void main(String[] args) {

        for (int i = 1; i <= 9; i ++) {
            for (int j = 1; j <= 9; j ++) {
                System.out.println(i + "*" + j + "=" + i * j);
            }            
        }
    }
}
