package Lesson7;

// Lesson7 練習問題
// 7-B-1

public class ArrayTest {
    public static void main(String[] args) {
        String[] tex = {"A", "B", "C", "D", "E", "F", "G", "H"};

        for (int i = 0; i < tex.length; i ++) {
            System.out.println(i + "番目の要素：" + tex[i]);
        }
    }
}
