public class Var2 {
    public static void main(String[] args) {
        int num1;
        num1 = 4321;        
        System.out.println("変数num1の値は" + num1 + "です。");
        
        long num2;
        num2 = 1234567891234L;
        System.out.println("変数num2の値は" + num2 + "です。");
    }
}
